package org.example;
public class SwipeGood {
    private int Id;
    private int good;
    private int bad;

    public int getId() {
        return Id;
    }
    public void setId(int Id) {
        this.Id = Id;
    }

    public int getGood() {
        return good;
    }
    public void setGood(int good) {
        this.good = good;
    }

    public int getBad() {
        return bad;
    }
    public void setBad(int bad) {
        this.bad = bad;
    }
}
